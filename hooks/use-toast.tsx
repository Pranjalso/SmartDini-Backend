type ToastProps = React.ComponentPropsWithoutRef<typeof Toast>
type ToastActionElement = React.ReactElement<typeof ToastAction>

export type { ToastProps, ToastActionElement }

type ToasterToast = ToastProps & {
  id: string
  title?: React.ReactNode
  description?: React.ReactNode
  action?: ToastActionElement
}

const TOAST_LIMIT = 1
const TOAST_REMOVE_DELAY = 1000

const toastState: ToasterToast[] = []
const listeners: Array<(state: State) => void> = []

type State = {
  toasts: ToasterToast[]
}

type ToastParams = Omit<ToasterToast, "id" | "open" | "onOpenChange">

function genId() {
  return Math.random().toString(36).slice(2)
}

function dispatch(action: Action) {
  switch (action.type) {
    case "ADD_TOAST": {
      const { toast } = action
      toastState.push(toast)
      if (toastState.length > TOAST_LIMIT) {
        toastState.shift()
      }
      break
    }
    case "UPDATE_TOAST": {
      const { toast } = action
      const idx = toastState.findIndex((t) => t.id === toast.id)
      if (idx > -1) {
        toastState[idx] = { ...toastState[idx], ...toast }
      }
      break
    }
    case "DISMISS_TOAST": {
      const { toastId } = action
      const idx = toastState.findIndex((t) => t.id === toastId)
      if (idx > -1) {
        toastState[idx].open = false
        setTimeout(() => {
          const rmIdx = toastState.findIndex((t) => t.id === toastId)
          if (rmIdx > -1) {
            toastState.splice(rmIdx, 1)
          }
          notify()
        }, TOAST_REMOVE_DELAY)
      }
      break
    }
    case "REMOVE_TOAST": {
      const { toastId } = action
      const idx = toastState.findIndex((t) => t.id === toastId)
      if (idx > -1) {
        toastState.splice(idx, 1)
      }
      break
    }
  }
  notify()
}

type Action =
  | { type: "ADD_TOAST"; toast: ToasterToast }
  | { type: "UPDATE_TOAST"; toast: Partial<ToasterToast> }
  | { type: "DISMISS_TOAST"; toastId?: string }
  | { type: "REMOVE_TOAST"; toastId?: string }

function notify() {
  listeners.forEach((l) => {
    l({ toasts: toastState })
  })
}

function toast({ ...props }: ToastParams) {
  const id = genId()

  const update = (props: ToasterToast) =>
    dispatch({
      type: "UPDATE_TOAST",
      toast: { ...props, id },
    })
  const dismiss = () => dispatch({ type: "DISMISS_TOAST", toastId: id })

  dispatch({
    type: "ADD_TOAST",
    toast: {
      ...props,
      id,
      open: true,
      onOpenChange: (open) => {
        if (!open) dismiss()
      },
    },
  })

  return {
    id: id,
    dismiss,
    update,
  }
}

function useToast() {
  const [state, setState] = React.useState<State>({ toasts: toastState })

  React.useEffect(() => {
    listeners.push(setState)
    return () => {
      const index = listeners.indexOf(setState)
      if (index > -1) {
        listeners.splice(index, 1)
      }
    }
  }, [state])

  return {
    ...state,
    toast,
    dismiss: (toastId?: string) => dispatch({ type: "DISMISS_TOAST", toastId }),
  }
}

export { useToast, toast, ToastProvider, ToastViewport, Toast, ToastTitle, ToastDescription, ToastClose, ToastAction }
